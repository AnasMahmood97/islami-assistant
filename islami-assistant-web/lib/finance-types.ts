export type FinanceTypeId = (typeof FINANCE_TYPES)[number];

/** تأمين تبادلي يُحسب على «إجمالي التمويل مع المرابحة» لمسار المرابحة، وعلى «قيمة التمويل» لمسار الإجارة */
export function useMurabahaTakafulBase(financeType: string): boolean {
  const hasMur =
    /مرابح|مرابحة|المرابح|المرابحة/.test(financeType) ||
    /تمويلات المرابح/i.test(financeType);
  const hasIja = /اجارة|الاجارة|اجاره|الاجاره/.test(financeType);
  if (hasMur) return true;
  if (hasIja) return false;
  return true;
}
